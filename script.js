//Set my age
let myAge = 23;

//A dog's early years
let earlyYears = 2;

let dogYears = (earlyYears * 10.5);

//A dog's later years
let laterYears = myAge - 2;

//My age in later dog years
var newAge = laterYears * 4; 

console.log(`If I were a dog, I would be ${newAge} years old.`);

//Calculate age in dog years
let myAgeInDogYears = newAge + dogYears;

//Putting my name in a string method which retuns it with lowercase letters
let myName = "Debbie".toLowerCase();

//Show my age in dog years
console.log(`My name is ${myName}. I am ${myAge} in human years, which is ${myAgeInDogYears} in dog years.`);


